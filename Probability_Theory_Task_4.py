"""
Задачу решать с помощью функции.
Есть ли статистически значимые различия в росте дочерей?
Рост матерей 172, 177, 158, 170, 178,175, 164, 160, 169, 165
Рост взрослых дочерей: 173, 175, 162, 174, 175, 168, 155, 170, 160
"""
from scipy import stats

# Данные роста матерей (удалено последнее значение)
mothers_heights = [172, 177, 158, 170, 178, 175, 164, 160, 169]  # Удалено 10-е значение

# Данные роста взрослых дочерей
daughters_heights = [173, 175, 162, 174, 175, 168, 155, 170, 160]

# Проводим парный t-тест
t_statistic, p_value = stats.ttest_rel(mothers_heights, daughters_heights)

print("t-статистика:", t_statistic)
print("p-значение:", p_value)

# Интерпретация результатов
alpha = 0.05  # Уровень значимости
if p_value < alpha:
    print("Есть статистически значимые различия в росте дочерей по сравнению с ростом их матерей.")
else:
    print("Нет статистически значимых различий в росте дочерей по сравнению с ростом их матерей.")
